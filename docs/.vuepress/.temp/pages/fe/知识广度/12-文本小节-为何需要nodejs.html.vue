<h2 id="对比其他语言" tabindex="-1"><a class="header-anchor" href="#对比其他语言" aria-hidden="true">#</a> 对比其他语言</h2>
<p>当年 Java 被发明使用时， C C++ 也发展了几十年了，为何 Java 还照样发展壮大起来呢？</p>
<p>以及近几年、现在，仍有多种新的语言被发明和使用，例如 swift golang Dart 等。</p>
<p>所以，nodejs 被使用不是个例，而是历史、现在、未来都发生的事情，它仅仅是其中的一件。</p>
<h2 id="技术的核心价值-——-提升效率" tabindex="-1"><a class="header-anchor" href="#技术的核心价值-——-提升效率" aria-hidden="true">#</a> 技术的核心价值 —— 提升效率</h2>
<p>如果你去做一个年终总结或者晋升述职，你对你的领导说：<br>
“我今年用了一个 xx 技术，非常厉害。最先进的技术，github stars 多少多少，国内外个大公司都在用，基于它来开发非常爽...”</p>
<p>说完，你的领导心里会有一个大大的问号：然后呢？这个技术给我降低了多少成本？带来了多少收益？—— 技术是生产力，技术的厉害最终都会体现到生产效率。<br>
让领导带着这个疑问，那你的年终奖或者晋升估计悬了。</p>
<p>现在你换一种说法：<br>
“我今年用了一个 xx 技术，非常厉害。这一年我们的项目工期降低了 xx ，项目 bug 率降低了 xx ，核算项目成本降低了 xx ，效率增加了 xx ...”<br>
然后把这个技术的优势展示一下，再展示一些统计数据。</p>
<p>说完，领导一看就觉得心里踏实了。</p>
<p>PS：不仅仅是软件技术，这个世界上任何技术、产品、制度流程、组织关系的存在，都是在优化效率。乃至全社会的经济发展，说白了就是生产效率。</p>
<h2 id="nodejs-如何提升效率" tabindex="-1"><a class="header-anchor" href="#nodejs-如何提升效率" aria-hidden="true">#</a> nodejs 如何提升效率</h2>
<p>网上说的 nodejs 的好处，大概都是：单线程，基于事件驱动，非阻塞，适合高并发服务。<br>
这些都是技术优势，就跟上文的第一个述职一样，没有体现任何生产效率的价值。</p>
<p>有同学可能会问：“适合高并发服务” 这不就是生产效率吗？—— 这是一个好问题<br>
但是，我们看问题得综合起来看。例如，你告诉 Java 工程师 nodejs 的好处，他们会用吗？—— 不会的，因为学习和切换技术栈需要大量的成本。</p>
<p>所以，nodejs 的关键在于它用了 JS 语法，而社会上有大量的熟悉 JS 的前端工程师。</p>
<ul>
<li>JS 语言不用学习，只需要了解 nodejs API 即可</li>
<li>前端工程师不做服务端，没有切换技术栈的历史包袱</li>
</ul>
<p>而前端工程师如果想要做服务端、做 webpack 等工具，nodejs 显然是他们最适合的技术方案，效率最高的方案。<br>
如果让他们再去学习 Java 等其他语言，这又是一大成本。</p>
<h2 id="前端工程师需要自己做服务端吗" tabindex="-1"><a class="header-anchor" href="#前端工程师需要自己做服务端吗" aria-hidden="true">#</a> 前端工程师需要自己做服务端吗？</h2>
<p>如果是一个公司级别的系统，庞大的项目，前端、客户端、服务端指责划分明确，只不需要前端工程师来开发服务端的。</p>
<p>但有些职能部门，需要开发一些企业内部的管理工具，或者一些小型的系统。此时再去找服务端的人，会遇到很多沟通障碍，特别是某些大公司，还有很多其他非技术的因素阻碍沟通。所以，预期困难的沟通还不如自己搞一个，反正也不会很复杂（相对于企业级的大系统后端来说）。</p>
<p>而且，自己开发了服务端，就可以争取到更多的资源和工作机会。领导很希望这样，因为这样可以扩大自己的退伍，有利于领导未来的晋升。</p>
<p>综合来看，在这些情况下，前端人员用 nodejs 自研服务端，是不是效率最高的方式呢？—— 答案很明显。</p>
<h2 id="总结" tabindex="-1"><a class="header-anchor" href="#总结" aria-hidden="true">#</a> 总结</h2>
<p>nodejs 有一定的技术优势，但它真正的优势在于使用 JS 语法，前端工程师学习成本低，能提高研发效率。</p>
