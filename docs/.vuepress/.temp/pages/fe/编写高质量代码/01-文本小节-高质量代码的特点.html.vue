<template><h1 id="高质量代码的特点" tabindex="-1"><a class="header-anchor" href="#高质量代码的特点" aria-hidden="true">#</a> 高质量代码的特点</h1>
<p>注：文本小节</p>
<p>软件工程师日常工作主要就是写代码，工作产出也是代码。代码是我们的一张亮，能写出高质量的代码，本身就应该是我们自己对自己的要求。</p>
<p>对于企业来讲，更希望能招聘到产出高质量代码的员工。企业的软件产品都是多个程序员合作写出来的，如果一旦有一位程序员产出代码质量不高，则会严重影响整个软件产品的质量。</p>
<p>很多同学面试完了之后，觉得自己写的代码也都对，正常执行也都没问题。但是最后面试没有通过，就很纳闷，觉得自己非技术方面有问题。其实不然，也许是你的代码是对的，但质量不高。</p>
<h2 id="规范性" tabindex="-1"><a class="header-anchor" href="#规范性" aria-hidden="true">#</a> 规范性</h2>
<p>记得前些年和一位同事（也是资深面试官）聊天时，他说到：一个候选人写完了代码，不用去执行，你打眼一看就知道他水平怎样。看写的是不是整洁、规范、易读，好的代码应该是简洁漂亮的，应该能明显的表达出人的思路。</p>
<p>代码规范性，包括两部分。<br>
第一，就是我们日常用 <a href="https://eslint.org/" target="_blank" rel="noopener noreferrer">eslint<ExternalLinkIcon/></a> 配置的规则。例如用单引号还是双引号，哪里应该有空格，行尾是否有分号等。这些是可以统一规定的。</p>
<p>第二，就是代码可读性和逻辑清晰性。
例如变量、函数的命名应该有语义，不能随便 <code v-pre>x1</code> <code v-pre>x2</code> 这样命名。再例如，一个函数超过 100 行代码就应该拆分一下，否则不易读。
再例如，一段代码如果被多个地方使用，应该抽离为一个函数，复用。像这些是 eslint 无法统一规定的，需要我们程序员去判断和优化。
再例如，在难懂的地方加注释。</p>
<p>PS：发现很多同学写英文单词经常写错，这是一个大问题。可以使用一些工具来做提醒，例如 <a href="https://marketplace.visualstudio.com/items?itemName=streetsidesoftware.code-spell-checker" target="_blank" rel="noopener noreferrer">vscode spell checker<ExternalLinkIcon/></a>。</p>
<h2 id="完整性" tabindex="-1"><a class="header-anchor" href="#完整性" aria-hidden="true">#</a> 完整性</h2>
<p>代码功能要完整，不能顾头不顾尾。例如，让你找到 DOM 节点子元素，结果只返回了 Element ，没有返回 Text 和 Comment 。</p>
<p>要保证代码的完整性，需要两个要素。第一，要有这个意识，并且去刻意学习、练习。第二，需要全面了解代码操作对象的完整数据结构，不能只看常用的部分，忽略其他部分。</p>
<h2 id="鲁棒性" tabindex="-1"><a class="header-anchor" href="#鲁棒性" aria-hidden="true">#</a> 鲁棒性</h2>
<p>“鲁棒”是英文 Robust 的音译，意思是强壮的、耐用的。即，不能轻易出错，要兼容一些意外情况。</p>
<p>例如你定义了一个函数 <code v-pre>function ajax(url, callback) {...}</code> ，我这样调用 <code v-pre>ajax('xxx', 100)</code> 可能就会报错。因为 <code v-pre>100</code> 并不是函数，它要当作函数执行会报错的。</p>
<p>再例如，一些处理数字的算法，要考虑数字的最大值、最小值，考虑数字是 <code v-pre>0</code> 或者负数。在例如，想要通过 <code v-pre>url</code> 传递一些数据，要考虑 <code v-pre>url</code> 的最大长度限制，以及浏览器差异。</p>
<p>PS：使用 Typescript 可以有效的避免类型问题，是鲁棒性的有效方式。</p>
<h2 id="总结" tabindex="-1"><a class="header-anchor" href="#总结" aria-hidden="true">#</a> 总结</h2>
<p>高质量代码的特点：</p>
<ul>
<li>规范性：符合代码规范，逻辑清晰可读</li>
<li>完整性：考虑全面所有功能</li>
<li>鲁棒性：处理异常输入和边界情况</li>
</ul>
