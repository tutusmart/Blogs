<template><h1 id="项目负责人的职责" tabindex="-1"><a class="header-anchor" href="#项目负责人的职责" aria-hidden="true">#</a> 项目负责人的职责</h1>
<p>注：文字小节</p>
<h2 id="题目" tabindex="-1"><a class="header-anchor" href="#题目" aria-hidden="true">#</a> 题目</h2>
<p>作为项目前端技术负责人，主要的职责是什么？</p>
<h2 id="目标" tabindex="-1"><a class="header-anchor" href="#目标" aria-hidden="true">#</a> 目标</h2>
<p>项目前端技术负责人，将负责和项目前端开发相关的所有事情，不仅仅是前端范围内的，也不仅仅是开发的事宜。</p>
<p>目标：保证项目按时、按质量的交付上线，以及上线之后的安全稳定运行。</p>
<h2 id="职责" tabindex="-1"><a class="header-anchor" href="#职责" aria-hidden="true">#</a> 职责</h2>
<h3 id="把控需求" tabindex="-1"><a class="header-anchor" href="#把控需求" aria-hidden="true">#</a> 把控需求</h3>
<p>新项目开始、或者新功能模块开始时要参与需求评审，认真审阅需求的详细内容，给出评审意见，提出问题。自己已经同意的需求要能保证按时、按质量的完成。</p>
<p>评审需求需要你能深入理解项目的业务，不仅仅是自己负责的功能，还有上下游全局的串联。所以，一入职的新人无论技术能力多好，都无法立刻作为项目技术负责人，他还需要一段时间的业务积累和熟练。PS：除非他在其他公司已经是这个方面的业务专家。</p>
<p>需求评审之后，还可能有 UI 设计图的评审，也要参与，提出自己的意见和问题。保证评审通过的 UI 设计图都能保质保量的开发出来。</p>
<p>需求和 UI 设计图评审完之后，还要给出开发的排期。此时要全面考虑，不仅仅要考虑开发时间，还有内部测试、单元测试的时间，以及考虑一些延期的风险，多加几天的缓冲期。</p>
<p>最后，在项目进行过程中，老板或者 PM 有可能中途插入新需求。此时要积极沟通，重新评估，还要争取延长项目开发周期。需求增加了，肯定周期也要延长一些。</p>
<h3 id="技术方案设计" tabindex="-1"><a class="header-anchor" href="#技术方案设计" aria-hidden="true">#</a> 技术方案设计</h3>
<blockquote>
<p>需求指导设计，设计指导开发。</p>
</blockquote>
<p>需求和 UI 设计图确定之后，要先进行技术方案设计，写设计文档，评审，通过之后再开发。技术方案设计应该包含核心数据结构的设计，核心流程的设计，核心功能模块的组织和实现。评审时看看这些有没有不合理、隐患、或者和别人开发重复了。</p>
<p>技术方案设计还要包括和其他对接方的，如和服务端、客户端的接口格式。也要叫他们一起参与评审，待他们同意之后再开发。</p>
<h3 id="开发" tabindex="-1"><a class="header-anchor" href="#开发" aria-hidden="true">#</a> 开发</h3>
<p>作为技术负责人，不应该把自己的主要精力放在代码开发上，但也不能完全不写代码。
应该去写一些通用能力，核心功能，底层逻辑的代码。其他比较简单的业务代码，可以交给项目成员来完成。</p>
<h3 id="监督代码质量" tabindex="-1"><a class="header-anchor" href="#监督代码质量" aria-hidden="true">#</a> 监督代码质量</h3>
<p>技术负责人，可能会带领好多人一起编写代码，但他要把控整个项目的代码质量。例如：</p>
<ul>
<li>制定代码规范</li>
<li>定期组织代码审核</li>
<li>CI 时使用自动化单元测试</li>
</ul>
<h3 id="跟踪进度" tabindex="-1"><a class="header-anchor" href="#跟踪进度" aria-hidden="true">#</a> 跟踪进度</h3>
<p>每天都组织 10 分钟站会，收集当前的进度、风险和问题。如有延期风险，要及时汇报。</p>
<p>不仅仅要关心前端开发的进度，还要关心上下游。例如上游的 UI 设计图延期，将会导致前端开发时间不够，进而导致测试时间不够，甚至整个项目延期。</p>
<h3 id="稳定安全的运行" tabindex="-1"><a class="header-anchor" href="#稳定安全的运行" aria-hidden="true">#</a> 稳定安全的运行</h3>
<p>上线之后，要能实时把控项目运行状态，是否稳定、安全的运行。万一遇到问题，要第一时间报警。</p>
<p>所以，项目中要增加各种统计和监控功能，例如流量统计、性能统计、错误监控，还有及时报警的机制。</p>
