</ul>
<h2 id="持续跟进" tabindex="-1"><a class="header-anchor" href="#持续跟进" aria-hidden="true">#</a> 持续跟进</h2>
<p>分析、解决、测试，都是在你本地进行，网站其他用户的情况你看不到。
所以要增加性能统计，看全局，不只看自己。</p>
<p>JS 中有 Performance API 可供获取网页的各项性能数据，对于性能统计非常重要。
如 <code v-pre>performance.timing</code> 可以获取网页加载各个阶段的时间戳。</p>
<p>如果你的公司没有内部的统计服务（一般只有大厂有），没必要自研，成本太高了。可以使用第三方的统计服务，例如阿里云 ARMS 。</p>
