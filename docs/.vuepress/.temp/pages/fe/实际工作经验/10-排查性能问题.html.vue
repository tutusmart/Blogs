<h2 id="前端性能指标" tabindex="-1"><a class="header-anchor" href="#前端性能指标" aria-hidden="true">#</a> 前端性能指标</h2>
<p>能搜索到的性能指标非常多，也有很多非标准的指标。最常用的指标有如下几个：</p>
<h3 id="first-paint-fp" tabindex="-1"><a class="header-anchor" href="#first-paint-fp" aria-hidden="true">#</a> First Paint (FP)</h3>
<p>从开始加载到浏览器<strong>首次绘制像素</strong>到屏幕上的时间，也就是页面在屏幕上首次发生视觉变化的时间。但此变化可能是简单的背景色更新或不引人注意的内容，它并不表示页面内容完整性，可能会报告没有任何可见的内容被绘制的时间。</p>
<h3 id="first-contentful-paint-fcp" tabindex="-1"><a class="header-anchor" href="#first-contentful-paint-fcp" aria-hidden="true">#</a> First Contentful Paint（FCP）</h3>
<p>浏览器<strong>首次绘制来自 DOM 的内容</strong>的时间，内容必须是文本、图片（包含背景图）、非白色的 canvas 或 SVG，也包括带有正在加载中的 Web 字体的文本。</p>
<h3 id="first-meaningful-paint-fmp" tabindex="-1"><a class="header-anchor" href="#first-meaningful-paint-fmp" aria-hidden="true">#</a> First Meaningful Paint（FMP）</h3>
<p>页面的<strong>主要内容</strong>绘制到屏幕上的时间。这是一个更好的衡量用户感知加载体验的指标，但无法统一衡量，因为每个页面的主要内容都不太一致。<br>
主流的分析工具都已弃用 FMP 而使用 LCP</p>
<h3 id="domcontentloaded-dcl" tabindex="-1"><a class="header-anchor" href="#domcontentloaded-dcl" aria-hidden="true">#</a> DomContentLoaded（DCL）</h3>
<p>即 <code v-pre>DOMContentLoaded</code> 触发时间，DOM 全部解析并渲染完。</p>
<h3 id="largest-contentful-paint-lcp" tabindex="-1"><a class="header-anchor" href="#largest-contentful-paint-lcp" aria-hidden="true">#</a> Largest Contentful Paint（LCP）</h3>
<p><strong>可视区域中最大的内容元素</strong>呈现到屏幕上的时间，用以估算页面的主要内容对用户可见时间。</p>
<h3 id="load-l" tabindex="-1"><a class="header-anchor" href="#load-l" aria-hidden="true">#</a> Load（L）</h3>
<p>即 <code v-pre>window.onload</code> 触发时间，页面内容（包括图片）全部加载完成。</p>
