<h2 id="示例" tabindex="-1"><a class="header-anchor" href="#示例" aria-hidden="true">#</a> 示例</h2>
<p>PS：工作中有保密协议，所以只能说一些开源的，但也决定具有参考价值。</p>
<p>以编辑器 <a href="https://www.wangeditor.com/v5/guide/display.html" target="_blank" rel="noopener noreferrer">toHtml<ExternalLinkIcon/></a> 的问题作为一个示例，找个功能比较好理解。</p>
<p>问题描述</p>
<ul>
<li>新版编辑器只能输入 JSON 格式内容，无非输入 html</li>
<li>旧版编辑器却只能输入 html 格式</li>
<li>影响：旧版编辑器无法直接升级到新版编辑器</li>
</ul>
<p>问题如何解决</p>
<ul>
<li>文档写清楚，争取大家的理解</li>
<li>给出一些其他的升级<a href="https://github.com/wangeditor-team/wangEditor-v5/issues/233" target="_blank" rel="noopener noreferrer">建议<ExternalLinkIcon/></a></li>
<li>后续会增加 <code v-pre>editor.dangerouslyInsertHTML</code> API 尽量兼容 html 格式</li>
</ul>
<p>自己的成长</p>
<ul>
<li>要考虑一个产品完整的输入输出，而不只考虑编辑功能</li>
<li>要考虑旧版用户的升级成本</li>
<li>要参考其他竞品的设计，尽量符合用户习惯</li>
</ul>
