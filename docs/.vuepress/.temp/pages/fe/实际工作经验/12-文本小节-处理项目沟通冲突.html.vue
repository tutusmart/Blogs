<p>注：文本小节</p>
<h2 id="题目" tabindex="-1"><a class="header-anchor" href="#题目" aria-hidden="true">#</a> 题目</h2>
<p>项目中有没有发生过沟通的冲突（和其他角色）？如何解决的</p>
<h2 id="分析" tabindex="-1"><a class="header-anchor" href="#分析" aria-hidden="true">#</a> 分析</h2>
<p>有项目有合作，有合作就有沟通，有沟通就有冲突，这很正常。哪怕你自己单独做一个项目，你也需要和你的老板、客户沟通。</p>
<p>面试官通过考察这个问题，就可以从侧面得知你是否有实际工作经验。
因为即便你是一个项目的“小兵”，不是负责人，你也会参与到一些沟通和冲突中，也能说出一些所见所闻。</p>
<p>当然，如果你之前是项目负责人，有过很多沟通和解决冲突的经验，并在面试中充分表现出来。
相信面试官会惊喜万分（前提是技术过关），因为“技术 + 项目管理”这种复合型人才非常难得。</p>
<h2 id="常见的冲突" tabindex="-1"><a class="header-anchor" href="#常见的冲突" aria-hidden="true">#</a> 常见的冲突</h2>
<ul>
<li>需求变更：PM 或者老板提出了新的需求</li>
<li>时间延期：上游或者自己延期了</li>
<li>技术方案冲突：如感觉服务端给的接口格式不合理</li>
</ul>
<h2 id="正视冲突" tabindex="-1"><a class="header-anchor" href="#正视冲突" aria-hidden="true">#</a> 正视冲突</h2>
<p>从个人心理上，不要看到冲突就心烦，要拥抱变化，正视冲突。冲突是项目的一部分，就像 bug 一样，心烦没用。</p>
<p>例如，PM 过来说要修改需求，你应该回答：<strong>“可以呀，你组织个会议讨论一下吧，拉上各位领导，因为有可能会影响工期。”</strong></p>
<p>再例如，自己开发过程中发现可能会有延期，要及早的汇报给领导：<strong>“我的工期有风险，因为 xxx 原因，不过我会尽量保证按期完成。”</strong><br>
千万不要不好意思，等延期了被领导发现了，这就不好了。</p>
<h2 id="解决冲突" tabindex="-1"><a class="header-anchor" href="#解决冲突" aria-hidden="true">#</a> 解决冲突</h2>
<p>合作引起的冲突，最终还是要通过沟通来解决。</p>
<p>一些不影响需求和工期的冲突，如技术方案问题，尽量私下沟通解决。实在解决不了再来领导开会。<br>
需求变更和时间延期一定要开会解决，会议要有各个角色决定权的领导去参与。</p>
<p>注意，无论是私下沟通还是开会，涉及到自己工作内容变动的，一定要有结论。
最常见的就是发邮件，一定要抄送给各位相关的负责人。这些事情要公开，有记录，不要自己偷偷的就改了。</p>
<h2 id="如何规避冲突" tabindex="-1"><a class="header-anchor" href="#如何规避冲突" aria-hidden="true">#</a> 如何规避冲突</h2>
<ul>
<li>预估工期留有余地</li>
<li>定期汇报个人工作进度，提前识别风险</li>
</ul>
