<template><h1 id="你的不足" tabindex="-1"><a class="header-anchor" href="#你的不足" aria-hidden="true">#</a> 你的不足</h1>
<h2 id="题目" tabindex="-1"><a class="header-anchor" href="#题目" aria-hidden="true">#</a> 题目</h2>
<p>你目前有何不足的地方？</p>
<h2 id="分析" tabindex="-1"><a class="header-anchor" href="#分析" aria-hidden="true">#</a> 分析</h2>
<p>如果你被问到这个问题，那恭喜你面试快要通过了。一般在 3-4 面，或者 hr 面试时会问道这个问题。<br>
无论是 hr 还是技术人员问，你都要从技术角度来回答这个问题，说自己技术上的不足。不要扯其他方面的，容易掉到坑里。</p>
<p>你不用担心 hr 听不懂技术，听不懂更好。</p>
<h2 id="不足-不要乱说" tabindex="-1"><a class="header-anchor" href="#不足-不要乱说" aria-hidden="true">#</a> 不足，不要乱说</h2>
<p>要限定一个范围</p>
<ul>
<li>技术方面的</li>
<li>非核心技术栈的，即有不足也无大碍</li>
<li>些容易弥补的，后面才能“翻身”</li>
</ul>
<p>错误的示范</p>
<ul>
<li>我爱睡懒觉、总是迟到 —— 非技术方面</li>
<li>我自学的 Vue ，但还没有实践过 —— 核心技术栈</li>
<li>我不懂 React —— 技术栈太大，不容易弥补</li>
</ul>
<p>正确的示范</p>
<ul>
<li>脚手架，我还在学习中，还不熟练</li>
<li>nodejs 还需要继续深入学习</li>
</ul>
<h2 id="最后-要把话题反转回来" tabindex="-1"><a class="header-anchor" href="#最后-要把话题反转回来" aria-hidden="true">#</a> 最后，要把话题反转回来</h2>
<p>不能只说不足，就截止了。一定要通过不足，来突出自己的解决方案，以及未来的预期。<br>
这样给人的印象是：正式了自己的不足 + 有学习的态度 —— 非常好！</p>
<h2 id="答案" tabindex="-1"><a class="header-anchor" href="#答案" aria-hidden="true">#</a> 答案</h2>
